#include<iostream>
using namespace std;
int main(){
    cout<<"Arithmetic Operators "<<endl;
    int a = 21;
    int b = 10 ;
    int sum = a+b;
    int diff = a-b;
    int mul = a*b;
    double div = (double)a/b;
    int rem = a%b;
    cout<<"Sum: "<<sum<<endl;
    cout<<"difference = "<<diff<<endl;
    cout<<"multiplication = "<<mul<<endl;
    cout<<"division = "<<div<<endl;
    cout<<"remainder = "<<rem<<endl;
    return 0; 

}